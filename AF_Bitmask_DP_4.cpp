void sum_check(int i, int mask)
{
    // debug(i, mask);
    if (i >= k + 1)
    {
        if (!mask)
        {
            cout << "YES" << endl;
            for (int m = 1; m <= k; m++)
            {
                int msk = lastSame[m];
                debug(msk);
                int cur = 1;
                string ans;
                while (msk != 0)
                {
                    if ((msk & 1) == 1)
                    {
                        ans = ans + bikriok[cur] + "+";
                    }
                    msk >>= 1;
                    cur++;
                }
                ans.pop_back();
                ans = ans + "->" + utpadok[m];
                cout << ans << endl;
            }
            exit(0);
        }
        return;
    }
    if (bSum[mask] < ind[utpadok[i]])
        return;
    if (visited[i][mask])
        return;
    for (int j = mask; j; j = (j - 1) & mask)
    {
        if (bSum[j] == ind[utpadok[i]] && !visited[i + 1][mask ^ j])
        {
            lastSame[i] = j;
            sum_check(i + 1, mask ^ j);
        }
    }
    visited[i][mask] = 1;
}
inline void solve()
{
    cin >> n >> k;
    for (int i = 1; i <= n; i++)
        cin >> bikriok[i];
    for (int i = 1; i <= k; i++)
        cin >> utpadok[i];
    for (int i = 0; i < (1 << n); i++)
    {
        for (int j = 0; j < n; j++)
        {
            if (i & (1LL << (j)))
                bSum[i] += ind[bikriok[j + 1]];
        }
    }
    sum_check(1, (1 << n) - 1);
    cout << "NO" << endl;
}

